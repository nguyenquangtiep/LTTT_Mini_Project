package bai2;

public class ShannonFanoNode {
	
	private char name;
	private int value;
	private String code;
	
	public ShannonFanoNode() {
		this.code = "";
	}

	public char getName() {
		return name;
	}

	public void setName(char name) {
		this.name = name;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}
	
	

}
